#include <iostream>
#include <cmath>

using namespace std;

int main()
{
  int a = 5;
  int b = 15;

  cout << "a == b: " << (a == b) << endl;
  cout << "a != b: " << (a != b) << endl;
  cout << "a < b: " << (a < b) << endl;
  cout << "a > b: " << (a > b) << endl;
  cout << "a <= b: " << (a <= b) << endl;
  cout << "a >= b: " << (a >= b) << endl;

  return 0;
}